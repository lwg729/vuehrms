<template>
    <div>
        <div>
            <el-input
                    size="small"
                    style="width: 300px;float: left;margin-right: 10px"
                    placeholder="添加职称.."
                    prefix-icon="el-icon-plus"
                    v-model="jl.name">
            </el-input>
            <el-select v-model="jl.titleLevel" placeholder="职称等级"
                       size="small" style="float: left;margin-right: 10px">
                <el-option
                        v-for="item in titleLevels"
                        :key="item"
                        :label="item"
                        :value="item">
                </el-option>
            </el-select>
            <el-button type="primary" round size="small"
                       style="display: block" @click="addJobLevel">添加
            </el-button>
        </div>
        <div style="margin-top: 10px">
            <el-table
                    size="small"
                    :data="jls"
                    border
                    stripe
                    style="width: 80%">
                <el-table-column
                        prop="id"
                        label="编号"
                        width="55">
                </el-table-column>
                <el-table-column
                        prop="name"
                        label="职称名称"
                        width="150">
                </el-table-column>
                <el-table-column
                        prop="titleLevel"
                        label="职称级别"
                        width="120">
                </el-table-column>
                <el-table-column
                        prop="createDate"
                        label="创建时间"
                        width="150">
                </el-table-column>
                <el-table-column
                        label="是否启用">
                    <template slot-scope="scope">
                        <el-tag type="success" v-if="scope.row.enabled">已启用</el-tag>
                        <el-tag type="danger" v-else>未启用</el-tag>
                    </template>
                </el-table-column>
                <el-table-column
                        label="操作">
                    <template slot-scope="scope">
                        <el-button size="small" @click="showEditView(scope.row)">编辑</el-button>
                        <el-button size="small" type="danger" @click="deleteHandler(scope.row)">删除</el-button>
                    </template>
                </el-table-column>
            </el-table>
        </div>
        <el-dialog
                title="提示"
                :visible.sync="dialogVisible"
                width="29%" center>
            <div align="center">
                <table class="updateJlInput">
                    <tr>
                        <td>
                            <el-tag>职称名</el-tag>
                        </td>
                        <td>
                            <el-input size="small" v-model="updateJl.name"></el-input>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <el-tag>职称级别</el-tag>
                        </td>
                        <td>
                            <el-select v-model="updateJl.titleLevel" placeholder="职称等级" size="small"
                                       style="margin-left: 5px;margin-right: 5px;" >
                                <el-option
                                        v-for="item in titleLevels"
                                        :key="item"
                                        :label="item"
                                        :value="item">
                                </el-option>
                            </el-select>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <el-tag>是否启用</el-tag>
                        </td>
                        <td>
                            <el-switch
                                    style="display: block"
                                    v-model="updateJl.enabled"
                                    active-text="启用"
                                    inactive-text="禁用">
                            </el-switch>
                        </td>
                    </tr>
                </table>
            </div>
            <span slot="footer" class="dialog-footer">
                <el-button size="small" @click="dialogVisible = false">取 消</el-button>
                <el-button size="small" type="primary" @click="doUpdate">确 定</el-button>
            </span>
        </el-dialog>
    </div>
</template>

<script>
    export default {
        name: "JobLevelManage",

        data() {
            return {
                dialogVisible: false,
                updateJl: {
                    name: '',
                    titleLevel: '',
                    enabled: true,
                },
                jl: {
                    name: '',
                    titleLevel: '',
                    enabled: true,
                },
                jls: [],
                titleLevels: [
                    '正高级',
                    '副高级',
                    '中级',
                    '初级',
                    '员级',
                ]
            }
        },
        mounted() {
            this.initJls();
        },
        methods: {
            initJls() {
                this.getRequest("/system/basic/jobLevel/").then(resp => {
                    this.jls = resp;
                    this.jl = {
                        name: '',
                        titleLevel: '',
                    }
                })
            },
            addJobLevel() {
                if (this.jl.name && this.jl.titleLevel) {
                    this.postRequest("/system/basic/jobLevel/", this.jl).then(resp => {
                        if (resp) {
                            this.initJls();
                            /*this.jl={
                                name: '',
                                titleLevel: '',
                            }*/
                        }
                    })
                } else {
                    this.$message.error("添加的字段不能为空");
                }
            },
            deleteHandler(data) {
                this.$confirm('此操作将永久删除[' + data.name + ']职称, 是否继续?', '提示', {
                    confirmButtonText: '确定',
                    cancelButtonText: '取消',
                    type: 'warning'
                }).then(() => {
                    this.deleteRequest("/system/basic/jobLevel/" + data.id).then(resp => {
                        if (resp) {
                            this.initJls();
                        }
                    })
                }).catch(() => {
                    this.$message({
                        type: 'info',
                        message: '已取消删除'
                    });
                });
            },
            showEditView(data) {
                Object.assign(this.updateJl,data);
                this.dialogVisible = true;
            },
            doUpdate() {
                this.putRequest("/system/basic/jobLevel/", this.updateJl).then(resp => {
                    if (resp) {
                        this.initJls();
                        this.dialogVisible = false;
                    }
                })
            },
        }
    }
</script>

<style>
    .updateJlInput {
        width: 240px;
        margin-left: 8px;
    }
</style>
